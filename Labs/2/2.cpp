const int R = 5;
using namespace std;
#include <iostream>
template <typename T>
bool insideCircle(T x, T y)
{
    return sqrt(x * x + y * y) < R && x < 0 && y > 0;
};
template <typename T>
bool insideTriangle(T x,T y)
{
    auto
        Ax = 0,
        Ay = 0,
        Bx = R,
        By = 0,
        Cx = R / 2,
        Cy = -R;
    double
        p1 = (Ax - x) * (By - Ay) - (Bx - Ax) * (Ay - y),
        p2 = (Bx - x) * (Cy - By) - (Cx - Bx) * (By - y),
        p3 = (Cx - x) * (Ay - Cy) - (Ax - Cx) * (Cy - y);
    return ((p1 > 0 && p2 > 0 && p3 > 0) ||
            (p1 < 0 && p2 < 0 && p3 < 0)) &&
           y < 0 && x > 0;
}
int main()
{
    double x, y;
    cin >> x >> y;
    cout << (insideTriangle(x, y) || insideTriangle(x, y)
                 ? "Yes"
                 : "No")
         << "!";
    system("PAUSE");
    return 0;
}

